

/** \brief Solicita introducir un numero al usuario
 *
 * \return int
 *
 */
int inputInt(){
    int number;
    printf("Ingrese un numero \n");
    scanf("%d", &number);
    return number;
}

/** \brief Solicita introducir un numero al usuario
 *
 * \return float
 *
 */
float inputFloat(){
    int number;
    printf("Ingrese un numero \n");
    scanf("%f", &number);
    return number;
}

/** \brief Solicita introducir una letra al usuario
 *
 * \return char
 *
 */
char inputChar(){
    int character;
    printf("Ingrese una letra \n");
    scanf("%d", &character);
    return character;
}
